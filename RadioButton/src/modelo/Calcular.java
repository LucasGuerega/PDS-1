package modelo;

public class Calcular {
	public int add(int num1, int num2) {
		return num1 + num2;
	}

	public int sub(int num1, int num2) {
		return num1 - num2;
	}

	public int multi(int num1, int num2) {
		return num1 * num2;
	}

	public int divi(int num1, int num2) {
		return num1 / num2;
	}

}
